//swtich case Tareas dias de la semana 

let dia: number = 4;

switch (dia) {
  case 1:
    console.log("lunes");
    break;
  case 2:
    console.log("martes");
    break;
  case 3:
    console.log("miércoles");
    break;
  case 4:
    console.log("jueves");
    break;
  case 5:
    console.log("viernes");
    break;
  case 6:
    console.log("sábado");
    break;
  case 7:
    console.log("domingo");
    break;
}